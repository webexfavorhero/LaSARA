    public function login()
    {
        $input = Request::all();

        $username = $input['username'];
        $password = $input['password'];

        $user = DB::table('users')->where('username', $username)->where('password', $password)->get();

        if ($user) {
            $status = 'Logged In';
        } else {
            $status = 'Failed Login';
        }

        return view('welcome', compact('username', 'password', 'status'));
    }

